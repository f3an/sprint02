let num = prompt("Enter number from 1 to 10");

if (num == 1) {
    alert("Back to square 1");
} else if (num == 2) {
    alert("Goody 2-shoes");
} else if (num == 3 || num == 6) {
    alert("Two's company, three's a crowd");
} else if (num == 4 || num == 9) {
    alert("Counting sheep");
} else if (num == 5) {
    alert("Take five");
} else if (num == 7) {
    alert("Seventh heaven");
} else if (num == 8) {
    alert("Behind the eight-ball");
} else if (num == 10) {
    alert("Cheaper by the dozen");
} else {
    alert("Input error");
}